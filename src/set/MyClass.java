package set;

public class MyClass {
	
	int a;
	
	String name;

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "MyClass [a=" + a + ", name=" + name + "]";
	}
	
	

}
